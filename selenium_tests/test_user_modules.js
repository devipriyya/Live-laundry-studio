async function testDryCleaning() {
  const helper = new TestHelper('Dry Cleaning');
  try {
    await helper.init();
    await helper.login(config.credentials.customer.email, config.credentials.customer.password);

    await helper.log('Navigating to Dry Cleaning');
    await helper.driver.get(`${config.baseUrl}/dashboard/dry-cleaning`);

    await helper.log('Selecting items');
    // Click '+' button for Suit (2-piece)
    const suitCard = await helper.driver.wait(until.elementLocated(By.xpath("//h4[contains(., 'Suit (2-piece)')]/following-sibling::div//button[2]")), 10000);
    await suitCard.click();

    await helper.log('Filling pickup details');
    const dateInput = await helper.driver.findElement(By.id('pickup-date'));
    const tomorrow = new Date();
    tomorrow.setDate(tomorrow.getDate() + 2);
    const dateStr = tomorrow.toISOString().split('T')[0];
    await dateInput.sendKeys(dateStr);

    const timeSelect = await helper.driver.findElement(By.tagName('select'));
    await timeSelect.sendKeys('10:00 AM - 11:00 AM');

    await helper.log('Filling address');
    await helper.driver.findElement(By.placeholderText('Street Address')).sendKeys('Selenium Test Street');
    await helper.driver.findElement(By.placeholderText('City')).sendKeys('Test City');
    await helper.driver.findElement(By.placeholderText('ZIP Code')).sendKeys('123456');

    await helper.log('Submitting form');
    await helper.driver.findElement(By.css('button[type="submit"]')).click();

    await helper.log('Verifying summary modal');
    await helper.driver.wait(until.elementLocated(By.xpath("//h3[contains(., 'Order Summary')]")), 10000);
    await helper.log('Dry Cleaning form submitted to summary', 'PASS');

  } catch (error) {
    await helper.log('Test Failed', 'FAIL', error.message);
  } finally {
    helper.generateHtmlReport();
    await helper.quit();
  }
}

