const { By, until, Key } = require('selenium-webdriver');
const TestHelper = require('./testHelper');
const config = require('./config');

async function testLostAndFound() {
  const helper = new TestHelper('Lost and Found');
  try {
    await helper.init();
    await helper.login(config.credentials.customer.email, config.credentials.customer.password);

    await helper.log('Navigating to Lost & Found');
    await helper.driver.get(`${config.baseUrl}/dashboard/lost-items`);

    await helper.log('Clicking "Report New Item"');
    const reportBtn = await helper.driver.wait(until.elementLocated(By.xpath("//button[contains(., 'Report New Item')]")), 10000);
    await reportBtn.click();

    await helper.log('Filling report details');
    await helper.driver.findElement(By.placeholderText('e.g. ORD-123456')).sendKeys('ORD-TEST-123');
    await helper.driver.findElement(By.placeholderText('e.g. Blue Cotton Shirt')).sendKeys('Selenium Test Shirt');
    await helper.driver.findElement(By.tagName('textarea')).sendKeys('This is a test report generated by Selenium.');

    await helper.log('Submitting report');
    await helper.driver.findElement(By.xpath("//button[contains(., 'Submit Report')]")).click();

    await helper.log('Verifying success message');
    await helper.driver.wait(until.elementLocated(By.xpath("//div[contains(., 'Report submitted successfully')]")), 10000);
    await helper.log('Lost and Found report submitted successfully', 'PASS');

  } catch (error) {
    await helper.log('Test Failed', 'FAIL', error.message);
  } finally {
    helper.generateHtmlReport();
    await helper.quit();
  }
}

